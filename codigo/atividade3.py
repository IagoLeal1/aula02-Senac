# Questão 1

notaAlunoTeste = float(input("digite a nota do teste: "))
notaAlunoProva = float(input("digite a nota da prova: "))

media = (notaAlunoProva + notaAlunoTeste) / 2

print("o resultado do aluno na prova foi:", notaAlunoProva,
      "o resultado do aluno no teste foi:", notaAlunoTeste,
      "e a sua média final foi:", media)

